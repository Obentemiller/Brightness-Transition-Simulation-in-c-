#include <iostream>
#include <conio.h>
#include <string>
#include <random>
#include <chrono>

/**
 * Generates and prints a random sequence of spaces and stars (*) between
 * an initial and final value, based on a given probability.
 * 
 * @param ini The starting value of the range.
 * @param fim The ending value of the range.
 * @param probabilityOfStar The probability (as a percentage out of 100)
 *        of printing a star (*) instead of a space.
 */
void randowblimp(int ini, int fim, int probabilityOfStar) {
    std::random_device rd; // Initialize a random device.
    std::mt19937 gen(rd()); // Seed the generator.
    std::uniform_int_distribution<> distrib(ini, fim); // Uniform distribution between ini and fim.

    for (int i = ini; i <= fim; i++) {
        if (distrib(gen) <= probabilityOfStar) {
            std::cout << "*"; // Print a star with the given probability.
        }
        else {
            std::cout << " "; // Otherwise, print a space.
        }
    }
    std::cout << std::endl; // New line after printing the sequence.
}

/**
 * Simulates a brightness transition effect by gradually increasing and then
 * decreasing the probability of printing stars (*), thus creating a visual
 * effect of brightness increasing and then decreasing.
 * 
 * @param speed Controls the rate of change in the brightness level. A higher
 *        value results in faster transitions.
 */
static void brightness_transition(int speed) {
    int i = 0; // Starting brightness level.
    while (i != 100) {
        i += speed; // Increase the brightness.
        randowblimp(0, 100, i); // Print the current state of brightness.
        std::this_thread::sleep_for(std::chrono::seconds(0)); // Small pause (this appears to be a mistake, as the pause duration is 0 seconds).
    }

    while (i != 0) {
        i -= speed; // Decrease the brightness.
        randowblimp(0, 100, i); // Print the current state of brightness.
        std::this_thread::sleep_for(std::chrono::seconds(0)); // Small pause (again, this appears to be a mistake).
    }
}

/**
 * Main function to run the brightness transition simulation.
 */
int main(){
    brightness_transition(2); // Start the brightness transition with a speed of 2.
}

